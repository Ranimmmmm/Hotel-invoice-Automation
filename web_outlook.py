import urllib.parse
import webbrowser

def open_outlook_web_email(to_email: str, subject: str, body: str, host: str = "live"):
    """
    Opens Outlook on the web with a new message prefilled.
    host: "live" (consumer) or "office" (Microsoft 365)
    """
    base = "https://outlook.live.com/mail/0/deeplink/compose" if host == "live" \
        else "https://outlook.office.com/mail/deeplink/compose"
    params = {
        "to": to_email or "",
        "subject": subject or "",
        "body": body or "",
    }
    url = base + "?" + urllib.parse.urlencode(params, quote_via=urllib.parse.quote)
    webbrowser.open(url)   # opens default browser (or a new tab)
